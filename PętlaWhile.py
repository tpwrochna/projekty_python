'''
Pętla While
'''

# 1. przygotowanie danych przed pętlą
# 2. jak długo pętla ma się wykonywać, kiedy ma się skończyć (nagłówek pętli)
# 3. co pętla będzie robić (ciało pętli)

a = 1 # przygotowanie

while a < 10: # jak długo ma się wyknywać = warunek końca
    print(a) # ciało pętli
    a += 1 # zmieniamy instrukcję sterującą  a = a + 1

print("Pętla się wykonała ", a, "razy")



